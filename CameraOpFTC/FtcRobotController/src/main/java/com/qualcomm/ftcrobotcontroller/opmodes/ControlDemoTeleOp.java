package com.qualcomm.ftcrobotcontroller.opmodes;

import com.qualcomm.ftcrobotcontroller.controls.Controls;
import com.qualcomm.robotcore.eventloop.opmode.OpMode;
import com.qualcomm.robotcore.hardware.DcMotor;
import com.qualcomm.robotcore.hardware.Servo;

/**
 * TeleOp Mode
 * <p>
 * Enables control of the robot via the gamepad
 */
public class ControlDemoTeleOp extends OpMode {

	DcMotor motorRight;
	DcMotor motorLeft;
	DcMotor armExtend;
	Servo gondola;

	Controls controls;

	/**
	 * Constructor
	 */
	public ControlDemoTeleOp() {

	}

	/*
	 * Code to run when the op mode is first enabled goes here
	 * 
	 * @see com.qualcomm.robotcore.eventloop.opmode.OpMode#start()
	 */
	@Override
	public void init() {


		/*
		 * Use the hardwareMap to get the dc motors and servos by name. Note
		 * that the names of the devices must match the names used when you
		 * configured your robot and created the configuration file.
		 */

		motorRight = hardwareMap.dcMotor.get("motor_2");
		motorLeft = hardwareMap.dcMotor.get("motor_1");
		motorLeft.setDirection(DcMotor.Direction.REVERSE);

		gondola = hardwareMap.servo.get("gondola");

		controls = new Controls(gamepad1, gamepad2);

	}

	/*
	 * This method will be called repeatedly in a loop
	 * 
	 * @see com.qualcomm.robotcore.eventloop.opmode.OpMode#run()
	 */
	@Override
	public void loop() {

		controls.readJoySticksAndSetMotors();

		// write the values to the motors
		motorRight.setPower(controls.motorLeftSpeed);
		motorLeft.setPower(controls.motorRightSpeed);

		armExtend.setPower(controls.armExtendSpeed);

		gondola.setPosition(controls.gondolaAngle);

		/*
		 * Send telemetry data back to driver station. Note that if we are using
		 * a legacy NXT-compatible motor controller, then the getPower() method
		 * will return a null value. The legacy NXT-compatible motor controllers
		 * are currently write only.
		 */
        telemetry.addData("gondola", "gondola:  " + String.format("%.2f", controls.gondolaAngle));
        telemetry.addData("left tgt pwr",  "left  pwr: " + String.format("%.2f", controls.motorLeftSpeed));
        telemetry.addData("right tgt pwr", "right pwr: " + String.format("%.2f", controls.motorRightSpeed));

	}

	/*
	 * Code to run when the op mode is first disabled goes here
	 * 
	 * @see com.qualcomm.robotcore.eventloop.opmode.OpMode#stop()
	 */
	@Override
	public void stop() {

	}
}
